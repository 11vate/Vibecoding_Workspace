/**
 * Ability Template System
 * Defines templates for generating abilities procedurally
 */

import type { AbilityType, AbilityTarget } from '../entities/Ability';
import type { StatusEffect } from '@/shared/types/status';
import { Rarity } from '@/shared/types/rarity';

/**
 * Ability Template - defines a pattern for generating abilities
 */
export interface AbilityTemplate {
  name: string;
  description: string;
  type: AbilityType;
  energyCost: number | null;
  cooldown: number | null;
  effects: AbilityEffectTemplate[];
  tags: string[];
  element?: string;
  rarity?: Rarity; // Minimum rarity required
}

/**
 * Effect template for ability generation
 */
export interface AbilityEffectTemplate {
  type: 'damage' | 'heal' | 'buff' | 'debuff' | 'status' | 'special';
  target: AbilityTarget;
  baseValue: number;
  valueRange?: [number, number]; // Min, max multiplier
  element?: string;
  statusChance?: number;
  statusType?: StatusEffect;
  statusDuration?: number;
  lifesteal?: number;
  scaling?: 'attack' | 'defense' | 'hp' | 'speed';
}

