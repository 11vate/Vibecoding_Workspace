  /**
   * Get all templates
   */
  static getAllTemplates(): AbilityTemplate[] {
    return [...this.templates];
  }

  /**
   * Get templates by type
   */
  static getTemplatesByType(type: AbilityType): AbilityTemplate[] {
    return this.templates.filter((t) => t.type === type);
  }

  /**
   * Get templates by element
   */
  static getTemplatesByElement(element: string): AbilityTemplate[] {
    return this.templates.filter((t) => t.element === element);
  }

  /**
   * Get templates by rarity (minimum)
   */
  static getTemplatesByRarity(rarity: Rarity): AbilityTemplate[] {
    return this.templates.filter((t) => !t.rarity || t.rarity <= rarity);
  }

  /**
   * Get templates by tags
   */
  static getTemplatesByTags(tags: string[]): AbilityTemplate[] {
    return this.templates.filter((t) => tags.some((tag) => t.tags.includes(tag)));
  }

  /**
   * Get random template
   */
  static getRandomTemplate(type?: AbilityType, element?: string, rarity?: Rarity): AbilityTemplate | null {
    let filtered = this.templates;

    if (type) {
      filtered = filtered.filter((t) => t.type === type);
    }
    if (element) {
      filtered = filtered.filter((t) => t.element === element);
    }
    if (rarity) {
      filtered = filtered.filter((t) => !t.rarity || t.rarity <= rarity);
    }

    if (filtered.length === 0) return null;

    return filtered[Math.floor(Math.random() * filtered.length)];
  }
